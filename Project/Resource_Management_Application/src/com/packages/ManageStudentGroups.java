    public void Load_Groups()
    {
       
        int c;
        
        try {
            pst = con.prepareStatement("SELECT * FROM groups");
            ResultSet rs = pst.executeQuery();
            
            ResultSetMetaData rsd = rs.getMetaData();
            c = rsd.getColumnCount();
            
            d = (DefaultTableModel)jTable1.getModel();
            d.setRowCount(0);
            
            
            while(rs.next())
            {
                Vector v2 = new Vector();
                
                for(int i = 1; i <= c; i++)
                {
                    
                    v2.add(rs.getString("id"));
                    v2.add(rs.getString("sem"));
                    v2.add(rs.getString("prog"));
                    v2.add(rs.getString("gnum"));
                    v2.add(rs.getString("snum"));
                    v2.add(rs.getString("gid"));
                    v2.add(rs.getString("sid"));

                }
                
                d.addRow(v2);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(ManageLecturers.class.getName()).log(Level.SEVERE, null, ex);
        }
    
    }  

