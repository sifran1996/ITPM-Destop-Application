    private void button2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button2ActionPerformed
        // TODO add your handling code here:
        
        d = (DefaultTableModel)jTable1.getModel();
        int selected = jTable1.getSelectedRow();
        
        int id = Integer.parseInt(d.getValueAt(selected, 0).toString());
        
        
        String sem = jComboBox1.getSelectedItem().toString();
        String prog = jTextField1.getText();
        String gnum = jSpinner1.getValue().toString();
        String snum = jSpinner2.getValue().toString();
        String gid = jTextPane2.getText();
        String sid = jTextPane3.getText();
        
        
        try {
            pst = con.prepareStatement("Update groups set sem = ?, prog = ?, gnum = ?, snum = ?, gid = ?, sid = ? where id = ? ");
            pst.setString(1, sem);
            pst.setString(2, prog);
            pst.setString(3, gnum);
            pst.setString(4, snum);
            pst.setString(5, gid);
            pst.setString(6, sid);
            pst.setInt(7, id);
            pst.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "Student Group Updated Successfully");
            
            jTextField1.setText("");
            jComboBox1.setSelectedIndex(-1);
            jTextPane2.setText("");
            jTextPane3.setText("");

            Load_Groups();
            
            
        } catch (SQLException ex) {
            Logger.getLogger(AddLecturer.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_button2ActionPerformed

