private void get_details_by_given_id() {
        try {

            String whr = " WHERE ";
            String Query = "id LIKE " + "'" + "%" + jTextField1.getText().trim() + "%" + "'";
            String Qu = "lecturer LIKE " + "'" + "%" + jTextField1.getText().trim() + "%" + "'";
            String strQry = "SELECT * FROM session " + whr + Query + " OR " + Qu;
            DefaultTableModel df = (DefaultTableModel) jTable1.getModel();
            df.setRowCount(0);
            pst = con.prepareStatement(strQry);
            ResultSet rs = pst.executeQuery();
            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("id"));
                v.add(rs.getString("lecturer"));
                v.add(rs.getString("lecturer2"));
                v.add(rs.getString("subject"));
                v.add(rs.getString("groupid"));
                v.add(rs.getString("tag"));
                df.addRow(v);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }
