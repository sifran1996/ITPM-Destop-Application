    private void button6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button6ActionPerformed
        // TODO add your handling code here:

        //update
        d = (DefaultTableModel) jTable1.getModel();
        int selected = jTable1.getSelectedRow();

        int id = Integer.parseInt(d.getValueAt(selected, 0).toString());

        String lecturer = jTextField2.getText();
        String lecturer2 = jTextField3.getText();
        String subject = jTextField4.getText();
        String groupid = jTextField5.getText();
        String tag = jTextField6.getText();

        try {
            pst = con.prepareStatement("Update session set lecturer = ?, lecturer2 = ?, subject = ?, groupid = ?, tag = ?  where id = ? ");
            pst.setString(1, lecturer);
            pst.setString(2, lecturer2);
            pst.setString(3, subject);
            pst.setString(4, groupid);
            pst.setString(5, tag);
            pst.setInt(6, id);
            pst.executeUpdate();

            JOptionPane.showMessageDialog(this, "Subjects Updated Successfully");

            Load_Sessions();

        } catch (SQLException ex) {
            Logger.getLogger(AddingSessions.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_button6ActionPerformed

