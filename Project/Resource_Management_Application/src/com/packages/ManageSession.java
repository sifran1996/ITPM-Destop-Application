    public void Load_Sessions() {

        int c;

        try {
            pst = con.prepareStatement("SELECT * FROM session");
            ResultSet rs = pst.executeQuery();

            ResultSetMetaData rsd = rs.getMetaData();
            c = rsd.getColumnCount();

            d = (DefaultTableModel) jTable1.getModel();
            d.setRowCount(0);

            while (rs.next()) {
                Vector v2 = new Vector();

                for (int i = 1; i <= c; i++) {

                    v2.add(rs.getString("id"));
                    v2.add(rs.getString("lecturer"));
                    v2.add(rs.getString("lecturer2"));
                    v2.add(rs.getString("subject"));
                    v2.add(rs.getString("groupid"));
                    v2.add(rs.getString("tag"));
                }

                d.addRow(v2);
            }

        } catch (SQLException ex) {
            Logger.getLogger(ManageLecturers.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

