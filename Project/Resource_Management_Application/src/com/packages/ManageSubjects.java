    private void button1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button1ActionPerformed
        // TODO add your handling code here:
        
        
        d = (DefaultTableModel)jTable1.getModel();
        int selected = jTable1.getSelectedRow();
        
        int id = Integer.parseInt(d.getValueAt(selected, 0).toString());
        
        
        String year = jComboBox1.getSelectedItem().toString();
        String sname = jTextField1.getText();
        String scode = jTextField2.getText();
        String lhours = jSpinner1.getValue().toString();
        String thours = jSpinner2.getValue().toString();
        String lbhours = jSpinner3.getValue().toString();
        String ehours = jSpinner4.getValue().toString();
        
        
        try {
            pst = con.prepareStatement("Update subjects set year = ?, sem = ?, sname = ?, scode = ?, lhours = ?, thours = ?, lbhours = ?, ehours = ? where id = ? ");
            pst.setString(1, year);
            if(jRadioButton1.isSelected()){
                sem = "1st Semester";
            }
            if(jRadioButton2.isSelected()){
                sem = "2nd Semester";
            }
            pst.setString(2, sem);
            pst.setString(3, sname);
            pst.setString(4, scode);
            pst.setString(5, lhours);
            pst.setString(6, thours);
            pst.setString(7, lbhours);
            pst.setString(8, ehours);
            pst.setInt(9, id);
            pst.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "Subjects Updated Successfully");
            
            jComboBox1.setSelectedIndex(-1);
            buttonGroup1.clearSelection();
            jRadioButton1.setSelected(false);
            jRadioButton2.setSelected(false);
            jTextField1.setText("");
            jTextField2.setText("");
            Load_Subjects();
            
            
        } catch (SQLException ex) {
            Logger.getLogger(AddLecturer.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
            
        
        
        
        
    }//GEN-LAST:event_button1ActionPerformed

