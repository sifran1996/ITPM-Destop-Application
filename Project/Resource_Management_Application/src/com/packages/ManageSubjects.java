    public void Load_Subjects()
    {
       
        int c;
        
        try {
            pst = con.prepareStatement("SELECT * FROM subjects");
            ResultSet rs = pst.executeQuery();
            
            ResultSetMetaData rsd = rs.getMetaData();
            c = rsd.getColumnCount();
            
            d = (DefaultTableModel)jTable1.getModel();
            d.setRowCount(0);
            
            
            while(rs.next())
            {
                Vector v2 = new Vector();
                
                for(int i = 1; i <= c; i++)
                {
                    
                    v2.add(rs.getString("id"));
                    v2.add(rs.getString("year"));
                    v2.add(rs.getString("sem"));
                    v2.add(rs.getString("sname"));
                    v2.add(rs.getString("scode"));
                    v2.add(rs.getString("lhours"));
                    v2.add(rs.getString("thours"));
                    v2.add(rs.getString("lbhours"));
                    v2.add(rs.getString("ehours"));
                }
                
                d.addRow(v2);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(ManageLecturers.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

